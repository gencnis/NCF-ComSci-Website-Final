/*
    This document is used in Faculty page to be able to hide the information when clicked on a button
*/

// Prof Eaton interests display/no display onclick function
function eatonInterest() {
var x = document.getElementById("eatonInterest");
if (x.style.display === "none") {
    x.style.display = "block";
} else {
    x.style.display = "none";
}
}

